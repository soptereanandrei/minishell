#include "libft.h"

char	*ft_strnew(size_t size)
{
	size_t	i;
	char	*s;

	if (!(s = (char*)malloc(size + 1)))
		return (NULL);
	i = 0;
	while (i <= size)
		*(s + i++) = '\0';
	return (s);
}
